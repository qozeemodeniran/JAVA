//while loop code
public class myWhileLoop {
	public static void main(String args[]){
		//running as long as i is less than 5...
		int i = 0;
		while(i < 5){
			System.out.println(i);
			i++;
		}
		System.out.print("\n\n");
		//do-while loop...
		int x = 0;
		do
		{
			System.out.println(x);
			x++;
		}while(x < 5);
	}
}
